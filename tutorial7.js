//Operators in JavaScript. Which include Arithmetic, Assignment, Comparison and Logical operators.
let a=5;
let b=6;
console.log(a+b);
console.log(a-b);
console.log(a*b);
console.log(a/b);
console.log(a%b);
// This is how you can use all the arithmetic operators in javascript.

//How to increments values a=a+1, a+=1, a++, ++a
//How to decrements values a=a-1, a-=1, a--, --a

let x=10;
x=x+2;
console.log(x);

x+=2;
console.log(x);

x++;
console.log(x);

++x;
console.log(x);

let y=10;
y=y-2;
console.log(y);

y-=2;
console.log(y);

y--;
console.log(y);

--y;
console.log(y);
// This is how you can use increment and decrement operators in javascript.