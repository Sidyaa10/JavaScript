//Assignment Operators =, +=, -=, *=, /=, %=

let x=5;
x+=2; //x=x+3
console.log(x);

x-=2;//x=x-2
console.log(x);

x*=2;//x=x*4
console.log(x);

x/=2;//x=x/2
console.log(x);

x%=2;//x=x%2
console.log(x);

//Comparison operators ==, ===, !=, !==, >,<,>=,<=

let a=6;
let b=7;

const answer= a>b;//greater than
console.log(answer);

const ans= a<b;//less than
console.log(ans);

const answ=a == b;//equal to (Loose comparison)
console.log(answ);

const answr=a !== b;//not equal to (strict comparison)
console.log(answr);

const answerr=a===b;//strictly equal to (strict comparison)
console.log(answerr);

const result=a!=b;//strictly not equal to (loose comparison)
console.log(result);

const res=a>=b;//greater than or equal to
console.log(res);

const resu=b<=a;//less than or equal to
console.log(resu);
