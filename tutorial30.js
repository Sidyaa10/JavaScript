//Template Literals
 // Template literal is a string that allows embedded expressions.
// You can use multi-line strings and string interpolation features with them.

let firstName='Sid';//string literals
let lastName='Kadam'; 

//let fullName= firstName +' '+ lastName; //concatenation of two strings using simple method.

let fullName=`${firstName} ${lastName}`; //Concatenation using template literal
console.log(fullName);

//Template literals provide an alternate syntax to the traditional strings by usin backticks(``) instead of double or single quotes. It enables to use string interpolation, multiline strings, and special characters without escaping.




